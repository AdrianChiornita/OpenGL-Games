Level::Level(int number)
{
	srand((unsigned int) time(NULL));
	this->number = number;

	this->brickstruct.totalbricks = 252;
	this->brickstruct.numberofbricksperline = 18;
	this->brickstruct.numberoflines = 14;
	this->brickstruct.realbricks = 0;

	this->walls.push_back(new Wall("leftwall", true));
	this->walls[0]->setTranslate(0, 80);

	this->walls.push_back(new Wall("rightwall", true));
	this->walls[1]->setTranslate((float)Level::resolution.x - 20, 80);

	this->walls.push_back(new Wall("topwall", false));
	this->walls[2]->setTranslate(0, (float)Level::resolution.y - 20);

	for (int rowindex = 0; rowindex < this->brickstruct.numberoflines; ++rowindex)
	{
		this->brickstruct.bricks.push_back(std::vector<Brick *>());

		for (int columnindex = 0; columnindex < this->brickstruct.numberofbricksperline; ++columnindex)
		{
			if (levels[this->number % Level::MAXLEV][rowindex][columnindex] == 1) {

				powertype random = nothing;
				if ((rowindex * this->brickstruct.numberoflines + columnindex) % 6 == 0) 
				{
					random = static_cast<powertype>(rand() % PowerUp::powers);
				}
				else {
					random = nothing;
				}

				int life = (int)(rand() % (this->number + 1) + 1);
				life = (life % 8 == 0) ? 1 : (life % 8);
				this->brickstruct.bricks[rowindex].push_back(new Brick(
					"brick_" + std::to_string(this->brickstruct.realbricks),
					life,
					new PowerUp(
						"powerup_" + std::to_string(this->brickstruct.realbricks),
						random,
						false
					)
				)
				);
				this->brickstruct.bricks[rowindex][columnindex]->setTranslate(Level::resolution.x / 2
					- (this->brickstruct.numberofbricksperline * (Brick::length + 5)) / 2
					+ columnindex * (Brick::length + 5),
					Level::resolution.y / 2
					+ (this->brickstruct.numberoflines * (Brick::width + 5)) / 2
					- rowindex * (Brick::width + 5));
				
				this->brickstruct.bricks[rowindex][columnindex]->getPowerUp()->setTranslate(this->brickstruct.bricks[rowindex][columnindex]->getTranslateX()
					+ (float)Brick::length / 2 - 7.5f,
					this->brickstruct.bricks[rowindex][columnindex]->getTranslateY()
					+ (float)Brick::width / 2 - 7.5f
				);

				this->brickstruct.realbricks++;
			}
			else {
				this->brickstruct.bricks[rowindex].push_back(nullptr);
			}
		}
	}
}

